# Data types define the kind of values that variables can
# hold and the operations that can be performed on them

# Numeric data types
integer_number = 42
float_number = 3.14
complex_number = 2 + 3j

# Text data type
text_string = "Hello, Python!"

# Boolean data type
is_true = True
is_person = False

# Sequence data types
list_data = [1, 2, 3]
tuple_data = (4, 5, 6)

# Set data type
set_data = {1, 2, 3}

# Mapping data type
dictionary_data = {"name": "John", "age": 25}

# NoneType
null_value = None
